"""Math operators."""


def add(x: int, y: int):
    """Add x to y."""
    return x + y


def sub(x: int, y: int):
    """Subtract y from x."""
    return x - y


def multiply(x: int, y: int):
    """Multiply x by y."""
    return x * y


def div(x: int, y: int):
    """Divide x by y."""
    return x / y


def modulus(x: int, y: int):
    """Divide x by y and return remainder. Use an arithmetic operator."""
    return x % y


def floor_div(x: int, y: int):
    """Divide x by y and floor the value. Use an arithmetic operator."""
    return x // y

